public class Student {
    // Private fields
    private int id;
    private String name;

    // Constructor to initialize the Student object
    public Student(int id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getter method for id
    public int getId() {
        return id;
    }

    // Getter method for name
    public String getName() {
        return name;
    }
    
}
